for i, (name, params) in enumerate(experiments):
    print(f"\n Looking for: {name}")

    # Smart match (handles weird filenames)
    match = [f for f in files if name in f and f.endswith(".zip")]

    if not match:
        print(f"Missing: {name}")
        continue

    model_file = match[0]
    model_path = os.path.join(models_dir, model_file)

    print(f"Found: {model_file}")

    start_time = time.time()

    env = create_env(n_stack=params["n_stack"], seed=43)
    model = DQN.load(model_path, env=env)

    # ---- Reward ----
    mean_reward, std_reward = evaluate_policy(
        model, env, n_eval_episodes=10, deterministic=True
    )

    # ---- Episode length ----
    episode_lengths = []
    for _ in range(10):
        obs = env.reset()
        done = False
        length = 0

        while not done:
            action, _ = model.predict(obs, deterministic=True)
            obs, reward, done, info = env.step(action)
            length += 1

        episode_lengths.append(length)

    mean_ep_length = np.mean(episode_lengths)
    elapsed_time = (time.time() - start_time) / 60

    print(f"{name}: {mean_reward:.2f} ± {std_reward:.2f}")

    # ---- Save row ----
    results.append({
        "Exp": f"Exp_{i+1}",
        "Policy": params["policy"],
        "lr": params["learning_rate"],
        "gamma": params["gamma"],
        "batch_size": params["batch_size"],
        "expl_fraction": params["exploration_fraction"],
        "expl_initial_eps": 1.0,
        "expl_final_eps": params["exploration_final_eps"],
        "Mean Reward": round(mean_reward, 2),
        "Std Reward": round(std_reward, 2),
        "Mean Ep Length": round(mean_ep_length, 2),
        "Time (min)": round(elapsed_time, 2),
        "Description": build_description(name, params, baseline_params)
    })

    env.close()


