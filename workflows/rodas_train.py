def build_description(name, params, baseline_params):
    changes = []

    for key in params:
        if key in baseline_params and params[key] != baseline_params[key]:
            changes.append(f"{key}: {baseline_params[key]} → {params[key]}")

    change_text = ", ".join(changes) if changes else "No changes"

    explanation_map = {
        "baseline": "Baseline configuration with default hyperparameters",
        "buffer_less": "Tests effect of smaller replay buffer (less memory diversity)",
        "more_train": "More frequent updates to learn faster",
        "less_train": "Less frequent updates to reduce overfitting",
        "gradient_clipping": "Stabilizes training by limiting gradient size",
        "architecture_control": "Tests impact of larger neural network",
        "gamma_low": "Focuses on short-term rewards",
        "gamma_mid": "Balances short-term and long-term rewards",
        "very_low_explore": "Reduces exploration, favors exploitation early",
        "batch_small": "Introduces noisier but faster updates"
    }

    explanation = explanation_map.get(name, "")
    return f"{explanation} | Changes: {change_text}"


